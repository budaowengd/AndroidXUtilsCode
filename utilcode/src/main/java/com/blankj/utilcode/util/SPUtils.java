    /**
     * Return the single {@link SPUtils} instance
     *
     * @param spName The name of sp.
     * @return the single {@link SPUtils} instance
     */
    public static SPUtils getInstance(String spName) {
        return getInstance(spName, Context.MODE_PRIVATE);
    }

    /**
     * Return the single {@link SPUtils} instance
     *
     * @param spName The name of sp.
     * @param mode   Operating mode.
     * @return the single {@link SPUtils} instance
     */
    public static SPUtils getInstance(String spName, final int mode) {
        if (isSpace(spName)) spName = "spUtils";
        SPUtils spUtils = SP_UTILS_MAP.get(spName);
        if (spUtils == null) {
            synchronized (SPUtils.class) {
                spUtils = SP_UTILS_MAP.get(spName);
                if (spUtils == null) {
                    spUtils = new SPUtils(spName, mode);
                    SP_UTILS_MAP.put(spName, spUtils);
                }
            }
        }
        return spUtils;
    }

    private SPUtils(final String spName) {
        sp = Utils.getApp().getSharedPreferences(spName, Context.MODE_PRIVATE);
    }

    private SPUtils(final String spName, final int mode) {
        sp = Utils.getApp().getSharedPreferences(spName, mode);
    }

    /**
     * 保存一个Map到默认的sp种
     */
    public void put(Map<String, Object> params, boolean isCommit) {
        SharedPreferences.Editor edit = sp.edit();
        for (Map.Entry<String, Object> entry : params.entrySet()) {
            String key = entry.getKey();
            Object value = entry.getValue();
            if (value == null) {
                continue;
            }
            String type = value.getClass().getSimpleName();
            if ("String".equals(type)) {
                edit.putString(key, (String) value);
            } else if ("Integer".equals(type)) {
                edit.putInt(key, (Integer) value);
            } else if ("Boolean".equals(type)) {
                edit.putBoolean(key, (Boolean) value);
            } else if ("Float".equals(type)) {
                edit.putFloat(key, (Float) value);
            } else if ("Long".equals(type)) {
                edit.putLong(key, (Long) value);
            }
        }
        if (isCommit) {
            edit.commit();
        } else {
            edit.apply();
        }
    }


    public static String getStringValue(String key) {
        return (String) getData(key, "");
    }

    public static int getIntValue(String key) {
        return (int) getData(key, -1);
    }

    public static long getLongValue(String key) {
        return (long) getData(key, -1L);
    }

    /**
     * 从默认的sp中(名字是空字符串),读取数据
     */
    public static Object getData(String key, Object defValue) {
        if (defValue == null) {
            throw new NullPointerException("SharedPreferences " + key + " 默认值不能为空");
        }
        SharedPreferences sp = SPUtils.getInstance().sp;
        String type = defValue.getClass().getSimpleName();
        // defValue为为默认值，如果当前获取不到数据就返回它
        if ("String".equals(type)) {
            return sp.getString(key, (String) defValue);
        } else if ("Integer".equals(type)) {
            return sp.getInt(key, (Integer) defValue);
        } else if ("Boolean".equals(type)) {
            return sp.getBoolean(key, (Boolean) defValue);
        } else if ("Float".equals(type)) {
            return sp.getFloat(key, (Float) defValue);
        } else if ("Long".equals(type)) {
            return sp.getLong(key, (Long) defValue);
        } else if (type.contains("Set")) {
            return sp.getStringSet(key, (Set<String>) defValue);
        }
        return "";
    }

